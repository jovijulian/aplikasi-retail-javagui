    public void cekLogin(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/dbtugas", "root", "");
            PreparedStatement ps = con.prepareStatement("SELECT level FROM user WHERE username=? AND password=?");
            ps.setString(1, txtuser.getText());
            ps.setString(2, String.valueOf(txtpass.getPassword()));
            
            ResultSet r = ps.executeQuery();
            if(r.next() == false){
                JOptionPane.showMessageDialog(this, " Username atau password salah. ");
            }else{
                session.setUsername(txtuser.getText());
                session.setLevel(level);
                FormMenu utama = new FormMenu();
                utama.setLevel(r.getString(1));
                
                utama.show();
                this.dispose();
            }
        }catch (Exception e){
        System.out.println(e);
    
    }
    }
