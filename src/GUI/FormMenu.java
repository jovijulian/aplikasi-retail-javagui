    public void setLevel (String l){
        level = l;
        if(level.equals("siswa")){
            jMenu2.hide();
            jMenu3.hide(); 
            jMenu4.hide();  
            jMenu6.hide();
            
        }else if(level.equals("petugas")){
            jMenu6.hide();
            jMenu5.hide();
        }else if(level.equals("guru")){
            jMenu1.hide();
            jMenu2.hide();
            jMenu3.hide(); 
            jMenu4.hide();  
            jMenu6.hide();
        }else if(level.equals("kepsek")){
            jMenu1.hide();
            jMenu4.hide();
            jMenu6.hide();
        }
    }
