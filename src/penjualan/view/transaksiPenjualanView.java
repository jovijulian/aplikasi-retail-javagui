    private void hitungTotalBayar(){
        int jmlRow = tabelTransaksi.getRowCount();
        int tot = 0;
        if(jmlRow == 0){
            return;
        }else{
            for(int i = 0; i< jmlRow; i++){
                int jmlTot = Integer.valueOf(String.valueOf(model.getValueAt(i, 4)));
                tot += jmlTot;
            }
        }
        total.setText(String.valueOf(tot));
    }
    
    
