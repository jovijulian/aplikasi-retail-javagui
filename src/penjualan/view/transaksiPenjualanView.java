    private void namaBarangItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_namaBarangItemStateChanged
        String kode = (String) namaBarang.getSelectedItem();
        String kodeFix = kode.substring(0,4);
        try{
            Statement ktB=koneksi.koneksi().createStatement();
            ResultSet rsKBr=ktB.executeQuery("select jumlah_barang,harga_barang from barang where id_barang = '"+ kodeFix +"'");
            while (rsKBr.next()){
                stok.setText(rsKBr.getString("jumlah_barang"));
                harga.setText(rsKBr.getString("harga_barang"));
            }
        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_namaBarangItemStateChanged

    private void namaPelangganItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_namaPelangganItemStateChanged
        String kode = (String) namaPelanggan.getSelectedItem();
        String kodeFix = kode.substring(0,4);
        try{
            Statement ktB=koneksi.koneksi().createStatement();
            ResultSet rsKBr=ktB.executeQuery("select alamat,nohp from pelanggan where idPelanggan = '"+ kodeFix +"'");
            while (rsKBr.next()){
                alamat.setText(rsKBr.getString("alamat"));
                nohp.setText(rsKBr.getString("nohp"));
            }
        }catch (Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_namaPelangganItemStateChanged

    private void simpanKeranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanKeranjangActionPerformed
        String kode = (String) namaBarang.getSelectedItem();
        String kodeFix = kode.substring(0,1);
        String namaBrg = kode.substring(2, kode.length()-0);
        int hrg = Integer.valueOf(harga.getText());
        int jumlah = Integer.valueOf(jml.getText());
        long subTot = hrg * jumlah;
        
        Object[] data = new Object[5];
        data[0] = kodeFix;
        data[1] = namaBrg;
        data[2] = harga.getText();
        data[3] = jml.getText();
        data[4] = subTot;
        
        model.addRow(data);
        this.hitungTotalBayar();
        this.kosongkanData();
    }//GEN-LAST:event_simpanKeranjangActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
       int delData = tabelTransaksi.getSelectedRow();
       if(delData == -1){
           return;
       }else{
           model.removeRow(delData);
           this.hitungTotalBayar();
       }
    }//GEN-LAST:event_hapusActionPerformed

